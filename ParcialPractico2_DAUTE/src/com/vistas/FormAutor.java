    public void llenarTabla() {
        ArrayList<Autor> lstAutor = autorDAO.mostrarAutor();
        String[] encabezados = {"ID", "Nombre", "Apellido", "Nacionalidad"};
        DefaultTableModel modeloTabla = new DefaultTableModel(null, encabezados);
        for (Autor datos : lstAutor) {
            Object[] data = {datos.getId_autor(), datos.getNombre(), datos.getApellido(), datos.getNacionalidad()};
            modeloTabla.addRow(data);
        }
        tblAutores.setModel(modeloTabla);
    }

    public void limpiarForm() {
        txtCodigo.setText("0");
        txtNombre.setText("");
        txtApellido.setText("");
        txtNacionalidad.setText("");
    }

    public Autor capturarDatos() {
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String nacionalidad = txtNacionalidad.getText();
        Autor p = new Autor(codigo, nombre, apellido, nacionalidad);
        return p;
    }

    public void llenarFormulario() {
        int row = tblAutores.getSelectedRow();
        txtCodigo.setText(tblAutores.getValueAt(row, 0).toString());
        txtNombre.setText(tblAutores.getValueAt(row, 1).toString());
        txtApellido.setText(tblAutores.getValueAt(row, 2).toString());
        txtNacionalidad.setText(tblAutores.getValueAt(row, 3).toString());
    }

