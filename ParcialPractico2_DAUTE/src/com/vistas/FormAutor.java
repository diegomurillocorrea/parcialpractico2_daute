    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
        if (tblAutores.getSelectedRow() != -1) {
            int res = autorDAO.modificarAutor(capturarDatos());
            if (res == 1) {
                JOptionPane.showMessageDialog(null, "Autor actualizado");
                llenarTabla();
                limpiarForm();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para modificar");
        }
    }//GEN-LAST:event_btnModificarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        if (tblAutores.getSelectedRow() != -1) {
            int resp = JOptionPane.showConfirmDialog(null, "Seguro que desea Eliminarlo?", "Eliminar registro", 0);
            if (JOptionPane.OK_OPTION == resp) {
                int res = autorDAO.eliminarAutor(capturarDatos());
                if (res == 1) {
                    JOptionPane.showMessageDialog(null, "Autor eliminado");
                    llenarTabla();
                    limpiarForm();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para eliminar");
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

